weather = "rainy"
case weather
when 'rainy'
puts "It's raining men, Hallelujah!"
when 'sunny'
puts "It's gonna be a hot one, don't forget your sunscreen."
when 'cloudy'
puts "It's cold, dark, and grey outside but no rain yet!"
when 'windy'
puts "Hold on to something or someone or you might be blown away to oz."
when 'foggy'
puts "Low visibility today."
end
